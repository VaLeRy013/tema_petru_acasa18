np = int(input("numarul de petale ale unei flori: "))
if (np % 5 == 0):
    print("Nu ma iubeste deloc")
elif (np % 5 == 1):
    print("Ma iubeste un pic")
elif (np % 5 == 2):
    print("Ma iubeste mult")
elif (np % 5 == 3):
    print("Ma iubeste cu pasiune")
elif (np % 5 == 4):
    print("Ma iubeste la nebunie")
